// 3. Counter Incrementer (Using do while loop )
// Make a counter variable is 0
var counter = 0;
// Define the step by which the counter will be incremented
var step = 1;
// Start a do-while loop
do {
    // Output the current value of counter
    console.log("Counter: ".concat(counter));
    counter += step;
} 
// Continue loop while the counter is less than 100
while (counter < 100);
